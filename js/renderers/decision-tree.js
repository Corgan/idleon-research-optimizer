// ===== SAVE CHANGES (update in-place, then advance or resim) =====
export function _dtSaveChanges() {
  if (!DT.editState || DT.modalNodeId === null) return;
  const node = _dtGetNode(DT.modalNodeId);
  if (!node) return;
  const s = DT.editState;

  // Start node: never edited in-place - always fork a new branch
  if (node.event === 'start') {
    const editState = _dtCloneState(s);
    // Decision child captures user's edits at t=0
    const decNode = _dtCreateNode(node.id, 'decision', 0, editState);
    decNode.autoInsight = node.autoInsight; // propagate toggle to decision node
    // Sim forward from that decision
    if (decNode.autoInsight) {
      const finalNode = _dtAdvanceAutoInsight(decNode);
      if (!finalNode) {
        alert('Could not advance (sim did not find a research level-up within limit).');
        _dtRenderTree();
        return;
      }
      _dtRenderTree();
      _dtOpenModal(finalNode.id);
      return;
    }
    const simState = _dtCloneState(editState);
    const result = _dtStepSim(simState);
    if (!result) {
      alert('Could not advance (sim did not find an event within limit).');
      _dtRenderTree();
      return;
    }
    const eventNode = _dtCreateNode(decNode.id, result.event, result.time, result.state);
    _dtRenderTree();
    _dtOpenModal(eventNode.id);
    return;
  }

  // Update current node's state in-place
  node.baseState = _dtCloneState(s);
  node.rLv = s.rLv;
  node.rExp = s.rExp;
  node.expHr = s.expHr;

  const children = _dtGetChildren(node.id);
  if (children.length === 0) {
    // No children yet â†’ auto-advance: sim to next event, create child, open it
    if (node.autoInsight) {
      // Auto-insight: loop through insight-ups, create hidden nodes, stop at research level-up
      const finalNode = _dtAdvanceAutoInsight(node);
      if (!finalNode) {
        alert('Could not advance (sim did not find a research level-up within limit).');
        _dtRenderTree();
        return;
      }
      _dtRenderTree();
      _dtOpenModal(finalNode.id);
    } else {
      const simState = _dtCloneState({...node.baseState, rLv: node.rLv, rExp: node.rExp, expHr: node.expHr});
      const result = _dtStepSim(simState);
      if (!result) {
        alert('Could not advance (sim did not find an event within limit).');
        _dtRenderTree();
        return;
      }
      const newNode = _dtCreateNode(node.id, result.event, result.time, result.state);
      _dtRenderTree();
      _dtOpenModal(newNode.id);
    }
  } else {
    // Has children â†’ re-sim all descendants
    _dtResimChildrenOf(node);
    _dtRebuildInsightTimelines();
    _dtRenderTree();
    _dtRenderModal();
  }
}

// Compute changes between a node and its parent
function _dtComputeChanges(node) {
  if (!node || node.parentId === null) return [];
  const parent = _dtGetNode(node.parentId);
  if (!parent) return [];
  const changes = [];
  const pgl = parent.baseState.gl, ngl = node.baseState.gl;
  // Grid changes
  const gridChanges = [];
  for (const idx of Object.keys(RES_GRID_RAW)) {
    const i = Number(idx);
    const pv = pgl[i] || 0, nv = ngl[i] || 0;
    if (nv !== pv) {
      const name = RES_GRID_RAW[i][0].replace(/_/g, ' ');
      gridChanges.push(`${name} ${pv}\u2192${nv}`);
    }
  }
  if (gridChanges.length > 0) changes.push('Grid: ' + gridChanges.join(', '));
  // Mag changes: count by slot+type
  const magKey = (md) => {
    const m = {};
    for (const mag of md) { const k = mag.slot + ':' + mag.type; m[k] = (m[k]||0)+1; }
    return m;
  };
  const pmk = magKey(parent.baseState.md), nmk = magKey(node.baseState.md);
  const allMagKeys = new Set([...Object.keys(pmk), ...Object.keys(nmk)]);
  let magDiffs = 0;
  for (const k of allMagKeys) { if ((pmk[k]||0) !== (nmk[k]||0)) magDiffs++; }
  if (magDiffs > 0) changes.push(`Mags: ${magDiffs} change${magDiffs>1?'s':''}`);
  // Shape changes
  const psp = parent.baseState.sp, nsp = node.baseState.sp;
  let shapeDiffs = 0;
  for (let i = 0; i < Math.max(psp.length, nsp.length); i++) {
    const ps = psp[i] || {}, ns = nsp[i] || {};
    if (ps.x !== ns.x || ps.y !== ns.y || ps.rot !== ns.rot) shapeDiffs++;
  }
  if (shapeDiffs > 0) changes.push(`Shapes: ${shapeDiffs} moved`);
  return changes;
}

