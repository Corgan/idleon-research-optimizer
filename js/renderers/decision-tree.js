// ===== DELETE NODE + DESCENDANTS =====
export function _dtDeleteNode(nodeId) {
  const targetId = nodeId != null ? nodeId : DT.modalNodeId;
  if (targetId === null) return;
  const node = _dtGetNode(targetId);
  if (!node || node.parentId === null) return; // can't delete root

  // Collect all descendant IDs
  const toDelete = new Set();
  function collect(id) {
    toDelete.add(id);
    const n = _dtGetNode(id);
    if (n) for (const cid of n.childIds) collect(cid);
  }
  collect(node.id);

  // Remove from parent's childIds
  const parent = _dtGetNode(node.parentId);
  if (parent) parent.childIds = parent.childIds.filter(cid => !toDelete.has(cid));

  // Remove from compare set
  for (const id of toDelete) _dtCompareSet.delete(id);

  // Remove from nodes array
  for (let i = _dtNodes.length - 1; i >= 0; i--) {
    if (toDelete.has(_dtNodes[i].id)) _dtNodes.splice(i, 1);
  }

  // If the deleted node was being edited, open parent instead
  if (DT.modalNodeId !== null && toDelete.has(DT.modalNodeId)) {
    _dtCloseModal();
    if (parent) _dtOpenModal(parent.id);
  }
  _dtRenderTree();
  _dtRenderComparison();
}

// ===== RE-SIM DOWNSTREAM (helper) =====
function _dtResimChildrenOf(parentNode) {
  // Copy childIds since auto-insight rebuild may modify the array
  const origChildIds = parentNode.childIds.slice();
  for (const childId of origChildIds) {
    const child = _dtGetNode(childId);
    if (!child) continue;

    if (child._autoInsightChild) {
      // Auto-insight chain detected - tear down and rebuild so node count/events stay correct
      // 1. Walk the chain to collect all hidden nodes + the final visible node
      const chainIds = [];
      let cur = child;
      while (cur && cur._autoInsightChild) {
        chainIds.push(cur.id);
        const kids = _dtGetChildren(cur.id);
        cur = kids.length > 0 ? kids[0] : null;
      }
      // cur = final visible node at end of chain (or null if chain ends abruptly)
      let savedChildIds = [];
      if (cur) {
        chainIds.push(cur.id);
        savedChildIds = cur.childIds.slice();
      }
      // 2. Clean up compare set for deleted nodes
      const chainSet = new Set(chainIds);
      for (const cid of chainIds) _dtCompareSet.delete(cid);
      // 3. Remove chain start from parent's childIds
      parentNode.childIds = parentNode.childIds.filter(id => id !== childId);
      // 4. Remove all chain nodes from _dtNodes
      for (let i = _dtNodes.length - 1; i >= 0; i--) {
        if (chainSet.has(_dtNodes[i].id)) _dtNodes.splice(i, 1);
      }
      // 5. Rebuild chain if auto-insight still enabled, otherwise single normal step
      let newTail = null;
      if (parentNode.autoInsight) {
        newTail = _dtAdvanceAutoInsight(parentNode);
      } else {
        // Auto-insight was turned off - replace chain with a single sim step
        const ps = _dtCloneState({...parentNode.baseState, rLv: parentNode.rLv, rExp: parentNode.rExp, expHr: parentNode.expHr});
        const r = _dtStepSim(ps);
        if (r) newTail = _dtCreateNode(parentNode.id, r.event, r.time, r.state);
      }
      // 6. Re-attach saved children to the new tail node and continue resimming
      if (newTail && savedChildIds.length > 0) {
        for (const scId of savedChildIds) {
          const sc = _dtGetNode(scId);
          if (sc) sc.parentId = newTail.id;
        }
        newTail.childIds.push(...savedChildIds);
        _dtResimChildrenOf(newTail);
      } else if (!newTail && savedChildIds.length > 0) {
        // Fallback: re-attach to parent and resim normally
        for (const scId of savedChildIds) {
          const sc = _dtGetNode(scId);
          if (sc) sc.parentId = parentNode.id;
        }
        parentNode.childIds.push(...savedChildIds);
        for (const scId of savedChildIds) {
          const sc = _dtGetNode(scId);
          if (sc) _dtResimChildrenOf(sc);
        }
      }
      continue;
    }

    // Normal resim for non-auto-insight children
    const parentState = _dtCloneState({...parentNode.baseState, rLv: parentNode.rLv, rExp: parentNode.rExp, expHr: parentNode.expHr});
    const result = _dtStepSim(parentState);
    if (result) {
      child.event = result.event;
      child.time = result.time;
      child.rLv = result.state.rLv;
      child.rExp = result.state.rExp;
      child.expHr = result.state.expHr;
      child.baseState = _dtCloneState(result.state);
    }
    _dtResimChildrenOf(child);
  }
}

// Rebuild insightTimeline on a node by walking back through its auto-insight ancestors
function _dtRebuildInsightTimelines() {
  for (const node of _dtNodes) {
    if (node._autoInsightChild || !node.insightTimeline) continue;
    // Walk backwards to collect the auto-insight chain
    const chain = [];
    let cur = node;
    while (cur.parentId !== null) {
      const p = _dtGetNode(cur.parentId);
      if (p && p._autoInsightChild) { chain.unshift(p); cur = p; }
      else break;
    }
    if (chain.length === 0) { node.insightTimeline = null; continue; }
    // cur is now the first auto-insight child; its parent is the chain origin
    const originParent = cur.parentId !== null ? _dtGetNode(cur.parentId) : null;
    let prevILArr = originParent ? originParent.baseState.il : [];
    const timeline = [];
    let cumT = 0;
    for (const cn of chain) {
      cumT += cn.time;
      for (let i = 0; i < cn.baseState.il.length; i++) {
        const prev = prevILArr[i] || 0;
        const now = cn.baseState.il[i] || 0;
        if (now > prev) {
          const monoTargets = [...getMonoObsSet(cn.baseState.md)];
          timeline.push({ time: cumT, obs: i, prevIL: prev, newIL: now, monoTargets, expHr: cn.expHr });
        }
      }
      prevILArr = cn.baseState.il;
    }
    node.insightTimeline = timeline.length > 0 ? timeline : null;
  }
}

