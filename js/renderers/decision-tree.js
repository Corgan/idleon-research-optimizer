// Build initial state from current globals
function _dtBuildInitState() {
  const rLv = researchLevel;
  const gl = gridLevels.slice();
  const so = shapeOverlay.slice();
  const md = magData.slice(0, magnifiersOwned).map(m => ({...m}));
  const il = insightLvs.slice();
  const ip = insightProgress.slice();
  const occ = occFound.slice();
  const sp = shapePositions.map(s => ({...s}));
  const ctx = makeCtx(gl);
  const expHr = simTotalExpWith(gl, so, md, il, occ, rLv, ctx);
  return {
    gl, so, md, il, ip, occ, sp, rLv, rExp: getResearchCurrentExp(), expHr
  };
}

// Create a node and add to tree
function _dtCreateNode(parentId, event, time, state) {
  const node = {
    id: DT.nextId++,
    parentId,
    childIds: [],
    event,             // 'start' | 'level-up' | 'insight-up' | 'level+insight'
    time,              // cumulative hours from root of this node's branch
    rLv: state.rLv,
    rExp: state.rExp,
    expHr: state.expHr,
    baseState: _dtCloneState(state),
    autoInsight: false,         // user toggle: auto-optimize insight until next research level-up
    _autoInsightChild: false,   // auto-generated by parent's autoInsight chain
    insightTimeline: null,      // [{time, obs, prevIL, newIL, monoTargets}] on the final level-up node
    _x: 0, _y: 0, _depth: 0,
    _branchIdx: 0,
  };
  _dtNodes.push(node);
  if (parentId !== null) {
    const parent = _dtGetNode(parentId);
    if (parent) parent.childIds.push(node.id);
  }
  return node;
}

// Step sim: run from a state to the next event, return event info + new state
// Uses event-driven jumps instead of fixed ticks.
function _dtStepSim(state) {
  let gl = state.gl.slice();
  let so = state.so.slice();
  let md = state.md.map(m => ({...m}));
  let il = state.il.slice();
  let ip = state.ip.slice();
  let occ = state.occ.slice();
  let rLv = state.rLv;
  let rExp = state.rExp;
  const ctx = makeCtx(gl);
  let curExpHr = simTotalExpWith(gl, so, md, il, occ, rLv, ctx);
  let currentTime = 0;

  for (let jump = 0; jump < 50000; jump++) {
    if (curExpHr <= 0) return null;

    // Time to next research level-up
    const expToLv = researchExpReq(rLv) - rExp;
    let jumpHrs = expToLv / curExpHr;

    // Time to next insight level-up
    const monoObs = getMonoObsSet(md);
    const hrsToIns = hrsToNextInsightLv(monoObs, md, il, ip, gl, so, ctx);
    if (hrsToIns < jumpHrs) jumpHrs = hrsToIns;
    if (jumpHrs <= 0) jumpHrs = 1e-9;
    if (jumpHrs > 1e8) return null;

    const jumpSec = jumpHrs * 3600;
    rExp += curExpHr / 3600 * jumpSec;
    currentTime += jumpHrs;

    // Advance insight
    let insightLeveledUp = advanceInsightLevels(monoObs, md, il, ip, gl, so, ctx, jumpHrs);

    // Check level-ups
    var _adv = advanceResearchLevel(rExp, rLv);
    rExp = _adv.rExp; rLv = _adv.rLv; var rLeveledUp = _adv.changed;

    // Auto-unlock obs
    if (rLeveledUp) {
      for (let oi = 0; oi < OCC_DATA.length; oi++) {
        if ((occ[oi] || 0) < 1 && OCC_DATA[oi].roll <= rLv) occ[oi] = 1;
      }
    }

    if (rLeveledUp || insightLeveledUp) {
      curExpHr = simTotalExpWith(gl, so, md, il, occ, rLv, ctx);
      const eventType = rLeveledUp && insightLeveledUp ? 'level+insight'
        : rLeveledUp ? 'level-up' : 'insight-up';
      return {
        event: eventType,
        time: currentTime,
        state: { gl, so, md, il, ip, occ, sp: state.sp.map(s => ({...s})), rLv, rExp, expHr: curExpHr }
      };
    }
  }
  return null;
}

// Auto-advance through insight level-ups, applying monocle reopt at each,
// until we hit a research level-up (or level+insight). Creates intermediate
// hidden nodes and records a timeline of insight switches.
function _dtAdvanceAutoInsight(parentNode) {
  const timeline = [];
  let curParentId = parentNode.id;
  let cumTime = 0;
  let curState = _dtCloneState({...parentNode.baseState, rLv: parentNode.rLv, rExp: parentNode.rExp, expHr: parentNode.expHr});

  for (let safety = 0; safety < 500; safety++) {
    const result = _dtStepSim(curState);
    if (!result) return null; // sim didn't find any event

    cumTime += result.time;

    if (result.event === 'level-up' || result.event === 'level+insight') {
      // Final node - this is the one the user sees
      const finalNode = _dtCreateNode(curParentId, result.event, result.time, result.state);
      finalNode.insightTimeline = timeline.length > 0 ? timeline : null;
      finalNode.autoInsight = true; // propagate so next advance also auto-optimizes
      return finalNode;
    }

    // insight-up: create hidden intermediate, apply monocle reopt, continue
    const mMax = magMaxForLevel(result.state.rLv);

    // Figure out which obs leveled up (compare to curState before this step)
    const insightChanges = [];
    for (let i = 0; i < result.state.il.length; i++) {
      const prev = curState.il[i] || 0;
      const now = result.state.il[i] || 0;
      if (now > prev) insightChanges.push({ obs: i, prevIL: prev, newIL: now });
    }

    // Apply monocle reoptimization
    result.state.md = chooseMonoTargets(
      {gl: result.state.gl, so: result.state.so, md: result.state.md, il: result.state.il, ip: result.state.ip, occ: result.state.occ, rLv: result.state.rLv, mMax},
      undefined, 72
    );
    const ctx = makeCtx(result.state.gl);
    result.state.expHr = simTotalExpWith(
      result.state.gl, result.state.so, result.state.md, result.state.il,
      result.state.occ, result.state.rLv, ctx
    );

    // Record what monocles are targeting now
    const monoTargets = [...getMonoObsSet(result.state.md)];

    // Record timeline entry
    for (const ic of insightChanges) {
      timeline.push({
        time: cumTime,
        obs: ic.obs,
        prevIL: ic.prevIL,
        newIL: ic.newIL,
        monoTargets: [...new Set(monoTargets)],
        expHr: result.state.expHr,
      });
    }

    // Create hidden intermediate node
    const interNode = _dtCreateNode(curParentId, result.event, result.time, result.state);
    interNode._autoInsightChild = true;

    // Advance
    curParentId = interNode.id;
    curState = _dtCloneState(result.state);
  }
  return null; // safety limit reached
}

// ===== TREE LAYOUT =====
function _dtIsNodeVisible(node) {
  if (node._autoInsightChild) return false; // always hidden (overrides global toggle)
  if (!DT.showLevelUpsOnly) return true;
  return node.event !== 'insight-up';
}
function _dtVisibleParent(node) {
  // Walk up to find nearest visible ancestor
  let cur = node.parentId !== null ? _dtGetNode(node.parentId) : null;
  while (cur && !_dtIsNodeVisible(cur)) cur = cur.parentId !== null ? _dtGetNode(cur.parentId) : null;
  return cur;
}
function _dtVisibleChildren(nodeId) {
  // Get all visible descendants reachable through hidden intermediate nodes
  const result = [];
  function collect(id) {
    for (const ch of _dtGetChildren(id)) {
      if (_dtIsNodeVisible(ch)) result.push(ch);
      else collect(ch.id);
    }
  }
  collect(nodeId);
  return result;
}
